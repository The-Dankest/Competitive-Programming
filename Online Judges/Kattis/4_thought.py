from math import ceil

ops = ['+', '-', '*', '/']
two = -1
thr = -1
for i in range(64):
    one = i % 4
    if i % 4 == 0:
        two += 1
        two = two % 4
    if i % 16 == 0:
        thr += 1
        thr = thr % 16
    one_op = ops[one]
    two_op = ops[two]
    thr_op = ops[thr]
    math_str = ""
    if one_op == 3 and two_op == 3 and thr_op == 3:
        math_str = "ceil(4 / ceil(4 / ceil(4 / 4)))"
    elif one_op == 3 and two_op == 3:
        math_str = "ceil(4 / ceil(4 / 4)) {} 4".format(thr_op)
    elif one_op == 3 and thr_op == 3:
        math_str = "ceil(4 / 4) {} ceil(4 / 4)".format(two_op)
    elif two_op == 3 and thr_op == 3:
        math_str = "4 {} ceil(4 / ceil(4 / 4))".format(one_op)
    else:
        math_str = "4 {} 4 {} 4 {} 4".format(one_op, two_op, thr_op)
    ans = eval(math_str)
    print('"4 {} 4 {} 4 {} 4": {},'.format(one_op, two_op, thr_op, ans))